//-Exersice 6
console.log('Exersice6 - For Looping\n');

// Definisikan variable input
var startCount = 1;
var endCount = 20;

// First looping
console.log('LOOPING PERTAMA');
for(;startCount<=endCount;startCount++){
    console.log(startCount + ' - I love coding');
}

// refresh the value of variable startCount and endCount
startCount = 1;
endCount = 20;

// Second looping
console.log('LOOPING KEDUA');
for(;endCount>=startCount;endCount--){
    console.log(endCount + ' I will become fullstack developer');
}

